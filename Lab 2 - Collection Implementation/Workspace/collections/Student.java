package collections;
public class Student{  
  public int rollno;  
  public String name;  
  public int age;
  public String reason;  
  public Student(int rollno,String name,int age,String reason){  
   this.rollno=rollno;  
   this.name=name;  
   this.age=age;
   this.reason = reason;  
  }  
}  